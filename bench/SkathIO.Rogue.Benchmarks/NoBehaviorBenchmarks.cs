using System.Threading.Tasks;
using BenchmarkDotNet.Attributes;
using Microsoft.Extensions.DependencyInjection;
using SkathIO.Rogue;

namespace SkathIO.Rogue.Benchmarks;

/// <summary>
/// Scenario 1 — core hot-path dispatch, zero behaviors, sync-completing handler. Compares Rogue,
/// MediatR, and martinothamar/Mediator as sibling methods in one class so BenchmarkDotNet emits a
/// ranked ratio report (PD-27), Rogue baseline.
/// </summary>
/// <remarks>
/// PD-12 / AC-6.2 note: the genuinely 0-alloc Rogue path is the concrete generated method
/// <c>RogueDispatcher.Send_PingRequest()</c>, NOT the <c>ISender.Send&lt;T&gt;()</c> interface path
/// (which boxes one <c>ValueTask&lt;T&gt;</c> by design). The generated <c>Send_PingRequest</c> is a
/// <c>private</c> method on the <c>internal sealed RogueDispatcherImpl</c> subclass — it is NOT
/// reachable from a consumer assembly (the bench project). So this class measures the public
/// <c>ISender.Send&lt;T&gt;</c> path, which is expected to report one box, not 0 B.
/// TODO(7.2.1 D4 / follow-up): add a concrete-path 0-alloc bench once the generator emits a *public*
/// per-request dispatch entry point (e.g. a public <c>Send_PingRequest</c> on a public dispatcher, or
/// a public extension). Until then the NFR-PERF-1 0-byte gate cannot be machine-verified end-to-end
/// from a consumer — see diary 2026-06-06 plan-change.
/// </remarks>
[MemoryDiagnoser]
public class NoBehaviorBenchmarks
{
    private ISender _rogue = null!;
    private global::MediatR.ISender _mediatR = null!;
    private global::Mediator.IMediator _mediator = null!;

    [GlobalSetup]
    public void Setup()
    {
        var rogueServices = new ServiceCollection();
        rogueServices.AddRogue();
        rogueServices.AddTransient<IRequestHandler<PingRequest, string>, PingHandler>();
        _rogue = rogueServices.BuildServiceProvider().GetRequiredService<ISender>();

        var mediatRServices = new ServiceCollection();
        mediatRServices.AddMediatR(cfg => cfg.RegisterServicesFromAssemblyContaining<MediatRPingHandler>());
        _mediatR = mediatRServices.BuildServiceProvider().GetRequiredService<global::MediatR.ISender>();

        // martinothamar: AddMediator() is generated by Mediator.SourceGenerator into this compilation.
        var mediatorServices = new ServiceCollection();
        mediatorServices.AddMediator();
        _mediator = mediatorServices.BuildServiceProvider().GetRequiredService<global::Mediator.IMediator>();
    }

    [Benchmark(Baseline = true)]
    public ValueTask<string> Rogue_NoBehavior()
        => _rogue.Send(new PingRequest("ping"));

    [Benchmark]
    public Task<string> MediatR_NoBehavior()
        => _mediatR.Send(new MediatRPingRequest("ping"));

    [Benchmark]
    public ValueTask<string> Mediator_NoBehavior()
        => _mediator.Send(new MedPingRequest("ping"));
}
